#include "stdio.h"

double Power(double x, int n);

int main() {
	double x;
	int n;
	if (scanf("%lf %d", &x, &n)) {
		printf("%lg", Power(x,n));
	}
	return 0;
}

double Power(double x, int n) {
	if (n == 0)
		return 1;
	if (n < 0)
		return (1 / Power(x, -n));

	if ((n % 2) == 0) {
		return (Power(x, n / 2) * Power(x, n / 2));
	} else {
		return x * Power(x, n - 1);
	}	

}